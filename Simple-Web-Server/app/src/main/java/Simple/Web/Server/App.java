    private void ApacheButtonButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ApacheButtonButtonActionPerformed

        String webDir = inputWeb.getText();
        String logDir = inputLog.getText();
        int port = Integer.parseInt(inputPort.getText());

        // Save preferences
        ConfigHandler.savePreferences(port, webDir, logDir);

        if (webServer != null && webServer.isRunning()) {
            webServer.stop();
            ApacheButton.setText("Start");
            serviceLabel.setText("X");
        } else {
            webServer = new WebServer(port, webDir, logDir, jTextArea1);
            webServer.start();
            ApacheButton.setText("Stop");
            serviceLabel.setText("âœ“");
        }
    }//GEN-LAST:event_ApacheButtonButtonActionPerformed

